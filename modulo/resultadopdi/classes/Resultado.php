<?php

class Resultado {
    
    private $codigo;
    private $meta_atingida;
    private $periodo;
    private $analiseCritica;
    private $meta;
    private $acao;

    public function __construct(){
       
    }
    
    public function setCodigo($codigo) {
        $this->codigo = $codigo;
    }

    public function getCodigo() {
        return $this->codigo;
    }

    public function setMetaAtingida($meta) {
        $this->meta_atingida = $meta;
    }

    public function getMetaAtingida() {
        return $this->meta_atingida;
    }

    public function setPeriodo($periodo) {
        $this->periodo = $periodo;
    }

    public function getPeriodo() {
        return $this->periodo;
    }

    public function setAnaliseCritica($analiseCritica) {
        $this->analiseCritica = $analiseCritica;
    }
    
    public function getAnaliseCritica() {
        return $this->analiseCritica;
    }
    
    public function setAcao($acao) {
        $this->acao = $acao;
    }
    
    public function getAcao() {
        return $this->acao;
    }
    
    public function setMeta(Meta $meta) {
        $this->meta = $meta;
    }
    
    public function getMeta() {
        return $this->meta;
    }
    
   
}

?>
